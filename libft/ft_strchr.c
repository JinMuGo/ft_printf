#include "libft.h"

int	ft_strchr(const char *s, int c)
{
	int	i;

	if ((char) c == '\0')
		return ((int)ft_strlen(s));
	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return (i);
		i++;
	}
	return (-1);
}
